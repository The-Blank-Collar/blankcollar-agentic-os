function AgentRail({
  agents,
  loading,
  error,
}: {
  agents: AgentSummary[];
  loading: boolean;
  error: Error | null;
}) {
  if (loading && agents.length === 0) return <Hint>Reading roster…</Hint>;
  if (error) return <HintError msg={error.message} />;
  if (agents.length === 0) return <Hint>No agents yet. Onboarding adds them.</Hint>;
  return (
    <>
      {agents.slice(0, 5).map((a) => (
        <div key={a.id} className="agent-item">
          <div className="sigil">
            <Sigil seed={a.id} size={30} />
          </div>
          <div>
            <div className="name">
              {a.name}
              <span className="role" style={{ marginLeft: 6 }}>{a.kind}</span>
            </div>
            <div className="activity">
              {a.is_active ? "live · ready" : "idle"}
            </div>
          </div>
          <span className={`dot ${a.is_active ? "live" : "idle"}`} />
        </div>
      ))}
    </>
  );
}

// ─── brain mini ────────────────────────────────────────────────────────

const BRAIN_RING: Record<BrainNodeKind, number> = {
  person: 12,
  agent: 22,
  goal: 32,
  capture: 40,
  tool: 46,
};

function BrainMini({ graph }: { graph: BrainGraph | null }) {
  const positions = useMemo(() => {
    if (!graph) return new Map<string, { x: number; y: number }>();
    const groups = new Map<BrainNodeKind, BrainNode[]>();
    for (const n of graph.nodes) {
      const arr = groups.get(n.kind) ?? [];
      arr.push(n);
      groups.set(n.kind, arr);
    }
    const out = new Map<string, { x: number; y: number }>();
    for (const [kind, items] of groups) {
      const r = BRAIN_RING[kind];
      items.forEach((n, i) => {
        const angle = (i / Math.max(1, items.length)) * Math.PI * 2;
        out.set(n.id, { x: 50 + Math.cos(angle) * r, y: 30 + Math.sin(angle) * r * 0.55 });
      });
    }
    return out;
  }, [graph]);

  if (!graph) return null;

  return (
    <svg viewBox="0 0 100 60" preserveAspectRatio="none" width="100%" height="100%">
      {graph.edges.slice(0, 40).map((e, i) => {
        const a = positions.get(e.from);
        const b = positions.get(e.to);
        if (!a || !b) return null;
        return (
          <line
            key={i}
            x1={a.x}
            y1={a.y}
            x2={b.x}
            y2={b.y}
            stroke="var(--line-2)"
            strokeWidth="0.3"
          />
        );
      })}
      {graph.nodes.slice(0, 50).map((n) => {
        const p = positions.get(n.id);
        if (!p) return null;
        return (
          <circle
            key={n.id}
            cx={p.x}
            cy={p.y}
            r={n.kind === "person" ? 1.4 : n.kind === "agent" ? 1.2 : n.kind === "goal" ? 1.0 : 0.7}
            fill={
              n.kind === "person"
                ? "var(--ink)"
                : n.kind === "agent"
                ? "var(--ink-2)"
                : n.kind === "goal"
                ? "var(--bg)"
                : "var(--muted)"
            }
            stroke={n.kind === "goal" ? "var(--ink)" : "none"}
            strokeWidth="0.4"
          />
        );
      })}
    </svg>
  );
}

