/** Lightweight markdown — handles `## headings`, `- bullets`, `**bold**`, paragraphs. */
function Markdown({ text }: { text: string }) {
  const lines = text.split("\n");
  const blocks: React.ReactNode[] = [];
  let para: string[] = [];
  let bullets: string[] = [];

  const flushPara = () => {
    if (para.length === 0) return;
    blocks.push(
      <p
        key={`p-${blocks.length}`}
        className="serif"
        style={{ fontSize: 16, lineHeight: 1.55, color: "var(--ink)", margin: "0 0 10px 0" }}
      >
        {renderInline(para.join(" "))}
      </p>,
    );
    para = [];
  };
  const flushBullets = () => {
    if (bullets.length === 0) return;
    blocks.push(
      <ul
        key={`ul-${blocks.length}`}
        style={{
          margin: "0 0 12px 0",
          paddingLeft: 18,
          fontSize: 13.5,
          lineHeight: 1.55,
          color: "var(--ink-2)",
        }}
      >
        {bullets.map((b, i) => (
          <li key={i}>{renderInline(b)}</li>
        ))}
      </ul>,
    );
    bullets = [];
  };

  for (const raw of lines) {
    const line = raw.trim();
    if (line.length === 0) {
      flushPara();
      flushBullets();
      continue;
    }
    if (line.startsWith("## ")) {
      flushPara();
      flushBullets();
      blocks.push(
        <h3
          key={`h-${blocks.length}`}
          className="eyebrow"
          style={{ marginTop: 14, marginBottom: 6 }}
        >
          {line.slice(3).trim()}
        </h3>,
      );
      continue;
    }
    if (line.startsWith("# ")) {
      flushPara();
      flushBullets();
      blocks.push(
        <h2
          key={`h-${blocks.length}`}
          className="serif"
          style={{
            fontSize: 22,
            fontWeight: 500,
            margin: "10px 0 8px 0",
            letterSpacing: "-0.01em",
          }}
        >
          {line.slice(2).trim()}
        </h2>,
      );
      continue;
    }
    if (line.startsWith("- ") || line.startsWith("* ")) {
      flushPara();
      bullets.push(line.slice(2).trim());
      continue;
    }
    flushBullets();
    para.push(line);
  }
  flushPara();
  flushBullets();

  return <div>{blocks}</div>;
}

function renderInline(text: string): React.ReactNode {
  const parts = text.split(/(\*\*[^*]+\*\*)/g);
  return parts.map((part, i) => {
    if (/^\*\*[^*]+\*\*$/.test(part)) {
      return (
        <strong key={i} style={{ fontWeight: 500, color: "var(--ink)" }}>
          {part.slice(2, -2)}
        </strong>
      );
    }
    return <span key={i}>{part}</span>;
  });
}

// ─── stat strip ────────────────────────────────────────────────────────

function StatStrip({
  summary,
  heartbeat,
}: {
  summary: GoalsSummary | null;
  heartbeat: HeartbeatResponse | null;
}) {
  const captures = seriesValues(heartbeat, "captures");
  const runs = seriesValues(heartbeat, "runs_completed");
  const failed = seriesValues(heartbeat, "runs_failed");

  const stats: { label: string; value: string; delta: string; deltaPos: boolean }[] = [
    {
      label: "Active goals",
      value: summary ? String(summary.by_status.active) : "—",
      delta: summary ? `${summary.total} total` : "—",
      deltaPos: true,
    },
    {
      label: "Decisions waiting",
      value: summary ? String(summary.by_kind.decision) : "—",
      delta: summary && summary.stalled_count > 0 ? `${summary.stalled_count} stalled` : "all on pace",
      deltaPos: !summary || summary.stalled_count === 0,
    },
    {
      label: "Runs (14d)",
      value: String(sum(runs)),
      delta: failed && sum(failed) > 0 ? `${sum(failed)} failed` : "no failures",
      deltaPos: !failed || sum(failed) === 0,
    },
    {
      label: "Captures (14d)",
      value: String(sum(captures)),
      delta: trend(captures),
      deltaPos: trendPositive(captures),
    },
  ];

  return (
    <div className="stats">
      {stats.map((s) => (
        <div key={s.label} className="stat">
          <div className="label">{s.label}</div>
          <div className="v num">{s.value}</div>
          <div className={`delta ${s.deltaPos ? "pos" : "neg"}`}>
            {s.deltaPos ? "↑" : "↓"} {s.delta}
          </div>
        </div>
      ))}
    </div>
  );
}

function seriesValues(h: HeartbeatResponse | null, kpi: string): number[] {
  return (h?.series.find((s) => s.kpi === kpi)?.points ?? []).map((p) => p.value);
}
function sum(xs: number[]): number {
  return xs.reduce((a, b) => a + b, 0);
}
function trend(xs: number[]): string {
  if (xs.length < 4) return "—";
  const half = Math.floor(xs.length / 2);
  const a = sum(xs.slice(0, half));
  const b = sum(xs.slice(half));
  if (a === 0 && b === 0) return "quiet";
  const delta = b - a;
  if (delta === 0) return "steady";
  return `${delta > 0 ? "+" : ""}${delta}`;
}
function trendPositive(xs: number[]): boolean {
  if (xs.length < 4) return true;
  const half = Math.floor(xs.length / 2);
  return sum(xs.slice(half)) >= sum(xs.slice(0, half));
}

// ─── pulse strip (replaces fake "this week's outcomes") ──────────────

function PulseStrip({
  heartbeat,
  summary,
}: {
  heartbeat: HeartbeatResponse | null;
  summary: GoalsSummary | null;
}) {
  const captures = seriesValues(heartbeat, "captures");
  const runs = seriesValues(heartbeat, "runs_completed");
  const failed = seriesValues(heartbeat, "runs_failed");
  const last7 = (xs: number[]) => xs.slice(-7);

  const tiles: { v: string; l: string; sub: string }[] = [
    {
      v: String(sum(last7(captures))),
      l: "Captures · 7d",
      sub: "What you handed the system",
    },
    {
      v: String(sum(last7(runs))),
      l: "Runs completed · 7d",
      sub: sum(last7(failed)) > 0 ? `${sum(last7(failed))} failed runs` : "Clean week so far",
    },
    {
      v: summary ? String(summary.by_kind.routine + summary.by_kind.standing) : "—",
      l: "On autopilot",
      sub: "Routines + standing goals",
    },
  ];

  return (
    <div
      style={{
        display: "grid",
        gridTemplateColumns: "repeat(3, 1fr)",
        gap: 0,
        border: "1px solid var(--line)",
        borderRadius: 6,
        overflow: "hidden",
      }}
    >
      {tiles.map((c, i) => (
        <div
          key={i}
          style={{
            padding: 18,
            borderRight: i < 2 ? "1px solid var(--line)" : 0,
            background: "var(--bg-1)",
          }}
        >
          <div className="eyebrow" style={{ marginBottom: 8 }}>{c.l}</div>
          <div
            className="num"
            style={{ fontSize: 26, fontWeight: 500, letterSpacing: "-0.02em" }}
          >
            {c.v}
          </div>
          <div className="tiny" style={{ marginTop: 4 }}>{c.sub}</div>
        </div>
      ))}
    </div>
  );
}

// ─── inbox rail ────────────────────────────────────────────────────────

function InboxRail({
  items,
  loading,
  error,
  onOpenGoal,
}: {
  items: InboxItem[];
  loading: boolean;
  error: Error | null;
  onOpenGoal: (id: string) => void;
}) {
  if (loading && items.length === 0) {
    return <Hint>Reading the inbox…</Hint>;
  }
  if (error) {
    return <HintError msg={error.message} />;
  }
  if (items.length === 0) {
    return <Hint>Nothing pending. Your day is clear.</Hint>;
  }
  return (
    <div className="card" style={{ padding: 0, overflow: "hidden" }}>
      {items.map((it, i) => (
        <div
          key={`${it.goal_id}-${i}`}
          onClick={() => onOpenGoal(it.goal_id)}
          style={{
            display: "grid",
            gridTemplateColumns: "auto 1fr auto",
            gap: 16,
            padding: "16px 18px",
            alignItems: "center",
            borderBottom: i < items.length - 1 ? "1px solid var(--line)" : 0,
            cursor: "pointer",
          }}
        >
          <div className="sigil" style={{ width: 36, height: 36 }}>
            <Sigil seed={it.goal_id} size={34} />
          </div>
          <div>
            <div style={{ fontSize: 13, fontWeight: 500 }}>
              {it.title}{" "}
              <span className="mono" style={{ color: "var(--muted)", fontSize: 11 }}>
                · {labelForKind(it.item_kind)}
              </span>
            </div>
            <div className="small" style={{ marginTop: 4 }}>
              {it.urgency === "urgent" ? "Urgent · " : ""}
              created {relativeTime(it.created_at)}
            </div>
          </div>
          <div className="stack-h">
            <button
              className="btn btn-sm"
              onClick={(e) => {
                e.stopPropagation();
                onOpenGoal(it.goal_id);
              }}
            >
              Review
            </button>
          </div>
        </div>
      ))}
    </div>
  );
}

function labelForKind(k: InboxItem["item_kind"]): string {
  switch (k) {
    case "approval":       return "Approval";
    case "decision":       return "Decision";
    case "blocked":        return "Blocked";
    case "routine_output": return "Routine output";
    case "draft":          return "Draft";
  }
}

// ─── goal list ─────────────────────────────────────────────────────────

function GoalList({
  goals,
  loading,
  error,
  onOpenGoal,
  emptyHint,
}: {
  goals: Goal[];
  loading: boolean;
  error: Error | null;
  onOpenGoal: (id: string) => void;
  emptyHint: string;
}) {
  if (loading && goals.length === 0) return <Hint>Loading goals…</Hint>;
  if (error) return <HintError msg={error.message} />;
  if (goals.length === 0) return <Hint>{emptyHint}</Hint>;
  return (
    <div className="goal-list">
      {goals.map((g) => (
        <GoalRow key={g.id} g={g} onOpenGoal={onOpenGoal} />
      ))}
    </div>
  );
}

function GoalRow({ g, onOpenGoal }: { g: Goal; onOpenGoal: (id: string) => void }) {
  const pct = progressPercent(g);
  const dueLabel =
    g.due_at !== null
      ? new Date(g.due_at).toLocaleDateString("en-US", { month: "short", day: "numeric" })
      : g.kind === "routine" && g.cron_expr
      ? g.cron_expr
      : "no date";
  const statusColor =
    g.status === "active"
      ? "pos"
      : g.status === "paused"
      ? "warn"
      : g.status === "achieved"
      ? "info"
      : "idle";
  const sub =
    g.description?.split("\n")[0]?.slice(0, 90) ??
    `${g.kind} · ${g.status}`;
  return (
    <div className="goal-row" onClick={() => onOpenGoal(g.id)}>
      <div className="gn">{g.id.slice(0, 6)}</div>
      <div>
        <div className="gtitle">{g.title}</div>
        <div className="gsub">{sub}</div>
      </div>
      <div className="gprog">
        <div className="progressbar">
          <i style={{ width: `${pct}%` }} />
        </div>
        <span style={{ width: 28, textAlign: "right" }}>{pct}%</span>
      </div>
      <div className="gowner">
        <div className="sigil" style={{ width: 22, height: 22 }}>
          <Sigil seed={g.owner_id ?? g.id} size={20} />
        </div>
        <span style={{ fontSize: 12 }}>{g.kind}</span>
      </div>
      <div className="gdue">
        <span className={`dot ${statusColor}`} style={{ marginRight: 6 }} />
        {dueLabel}
      </div>
      <div className="gmore">
        <I name="chev" size={14} />
      </div>
    </div>
  );
}

// ─── heartbeat rail ────────────────────────────────────────────────────

function HeartbeatRail({
  q,
  loading,
  error,
}: {
  q: HeartbeatResponse | null;
  loading: boolean;
  error: Error | null;
}) {
  if (loading && !q) return <Hint>Reading pulse…</Hint>;
  if (error) return <HintError msg={error.message} />;
  if (!q) return null;

  return (
    <>
      {HEARTBEAT_SHOWN.map((s) => {
        const series = q.series.find((x) => x.kpi === s.kpi);
        if (!series) return null;
        const total = sum(series.points.map((p) => p.value));
        return (
          <div key={s.kpi} className="hb-row">
            <div>
              <div className="hbn">{series.label}</div>
              <div className="hbsub">
                {total} {series.unit} ·{" "}
                <span style={{ color: trendColor(series, s.positive) }}>{trend(series.points.map((p) => p.value))}</span>
              </div>
            </div>
            <Spark data={series.points.map((p) => p.value)} positive={s.positive} />
          </div>
        );
      })}
    </>
  );
}

function trendColor(s: HeartbeatSeries, kpiPositive: boolean): string {
  const xs = s.points.map((p) => p.value);
  const up = trendPositive(xs);
  // For positive KPIs (like captures), up means good. For runs_failed, up is bad.
  const isGood = kpiPositive ? up : !up;
  return isGood ? "var(--pos)" : "var(--warn)";
}

function Spark({ data, positive }: { data: number[]; positive: boolean }) {
  const max = Math.max(1, ...data);
  return (
    <div className="spark">
      {data.map((v, i) => (
        <i
          key={i}
          style={{
            height: `${Math.max(6, (v / max) * 100)}%`,
            background: positive ? "var(--ink-2)" : "var(--warn)",
            opacity: 0.4 + (i / Math.max(1, data.length)) * 0.6,
          }}
        />
      ))}
    </div>
  );
}

